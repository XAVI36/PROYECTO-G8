    private void cargarDatos() {
        
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //nombre de las columnas para la tabla clientes
        String titulos[] = {"ID","PRECIO TOTAL","CUOTAS","INTERESES","MATRICULA","VENDEDOR","FECHA ESCRITURA"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la base de datos 
        List <Venta> listaVentas = control.listaVentas();
        
        if(listaVentas!=null){
            for(Venta ven: listaVentas){
                Object[] objeto = {ven.getId(),ven.getPrecioTotal(),ven.getCuotas(),ven.getInteres(),ven.getMatricula(),ven.getVendedor(),ven.getFechaEscritura()};
                modeloTabla.addRow(objeto);
            }
        }
        
        TablaVentas.setModel(modeloTabla);
    }
