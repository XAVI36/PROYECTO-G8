    private void btnEliminarInmuebleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarInmuebleActionPerformed
        
        if(TablaInmuebles.getRowCount()>0){
            if(TablaInmuebles.getSelectedRow()!=-1){
                int id = Integer.parseInt(String.valueOf(TablaInmuebles.getValueAt(TablaInmuebles.getSelectedRow(),0)));
                control.eliminarInmueble(id);   
                
                mostrarMensaje("Inmueble eliminado correctamente","Info","Se elimino");
                cargarTabla();
            }
            else {
                mostrarMensaje("No selecciono ningun Inmueble","Error","Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla","Error","Error al eliminar");
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarInmuebleActionPerformed

    private void btnModificarInmuebleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarInmuebleActionPerformed
        if(TablaInmuebles.getRowCount()>0){
            if(TablaInmuebles.getSelectedRow()!=-1){
                int id = Integer.parseInt(String.valueOf(TablaInmuebles.getValueAt(TablaInmuebles.getSelectedRow(),0)));
                ModificarInmueble modi = new ModificarInmueble(id);
                modi.setVisible(true);
                modi.setLocationRelativeTo(null);
                dispose();
                
                
            }
            else {
                mostrarMensaje("No selecciono ningun Inmueble","Error","Error al eliminar");
            }
        }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla","Error","Error al eliminar");
}
    }//GEN-LAST:event_btnModificarInmuebleActionPerformed
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if (tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog  = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    
}
