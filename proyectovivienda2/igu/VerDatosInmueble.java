    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //nombre de las columnas para la tabal inmuebles
        String titulos[] = {"ID","Valor","TipoUnidad","Area"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la base de datos 
        List <Inmueble> listaInmuebles = control.traerInmuebles();
        
        if(listaInmuebles!=null){
            for(Inmueble inm: listaInmuebles){
                Object[] objeto = {inm.getId(),inm.getTipoUnidad(), inm.getValor(), inm.getArea()};
                modeloTabla.addRow(objeto);
            }
        }
        
        TablaInmuebles.setModel(modeloTabla);
    }
    
    
