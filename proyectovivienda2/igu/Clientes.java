    private void btnRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarClienteActionPerformed
        
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String sisben = txtSisben.getText();
        String subsidioMinisterio = txtSubsidioMinisterio.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        
        if (cedula.isEmpty() || nombre.isEmpty() || direccion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cedula, Nombre y Direcci√≥n son obligatorios.");
            return;
        }

        control.registrarCliente(cedula, nombre, sisben, subsidioMinisterio, direccion, telefono);

        JOptionPane.showMessageDialog(this, "Cliente registrado exitosamente.");

        
        txtCedula.setText("");
        txtNombre.setText("");
        txtSisben.setText("");
        txtSubsidioMinisterio.setText("");
        txtDireccion.setText("");// TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarClienteActionPerformed

