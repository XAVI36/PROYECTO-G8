    private void cargarDatos() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //nombre de las columnas para la tabla clientes
        String titulos[] = {"ID","VALOR","FECHA"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la base de datos 
        List <Pago> listaPagos = control.listaPagos();
        
        if(listaPagos!=null){
            for(Pago pag: listaPagos){
                Object[] objeto = {pag.getId(),pag.getValor(),pag.getFecha()};
                modeloTabla.addRow(objeto);
            }
        }
        
        TablaPagos.setModel(modeloTabla);
    }
